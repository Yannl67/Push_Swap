#include "../includes/libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*s;
	size_t	len;

	if (!nmemb || !size)
		return (malloc(0));
	len = nmemb * size;
	if (size && len / size != nmemb)
		return (NULL);
	s = malloc(nmemb * size);
	if (!s)
		return (NULL);
	ft_bzero(s, nmemb * size);
	return (s);
}
